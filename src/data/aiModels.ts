export const getModelsByCategory = (category: AICategory): AIModel[] => {
  return aiModels
    .filter(model => model.category === category)
    .sort((a, b) => {
      // Gratuits d'abord
      if (a.isFree && !b.isFree) return -1;
      if (!a.isFree && b.isFree) return 1;
      // Puis actifs
      if (a.apiStatus === 'active' && b.apiStatus !== 'active') return -1;
      if (a.apiStatus !== 'active' && b.apiStatus === 'active') return 1;
      // Puis inactifs
      return 0;
    });
};

