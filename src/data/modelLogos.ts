// Fonction pour obtenir le logo d'un modèle
export function getModelLogo(modelId: string, provider?: string): string {
  // Essayer avec l'ID du modèle d'abord
  if (modelLogos[modelId]) {
    return modelLogos[modelId];
  }
  
  // Essayer avec le provider en minuscules
  if (provider) {
    const providerKey = provider.toLowerCase().replace(/\s+/g, '');
    if (modelLogos[providerKey]) {
      return modelLogos[providerKey];
    }
    
    // Essayer des variations du provider
    const providerWords = provider.toLowerCase().split(/[\s\-_]+/);
    for (const word of providerWords) {
      if (modelLogos[word]) {
        return modelLogos[word];
      }
    }
  }
  
  return modelLogos.default;
}
